#define _GNU_SOURCE

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <sys/types.h>
#include <sys/utsname.h>

int main(int argc, char **argv)
{
    struct utsname un;
    uname(&un);

    if (argc > 1){
        printf("%s %s %s %s %s %s\n", un.sysname, un.nodename, un.release, un.version, un.machine, un.domainname);

    }else{
        printf("%s\n", un.sysname);
    }

    return EXIT_SUCCESS;
}
