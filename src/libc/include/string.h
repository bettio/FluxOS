#ifndef _SIZE_T_TYPE
#define _SIZE_T_TYPE
	typedef unsigned int size_t;
#endif

extern size_t strlen(const char *s);
extern int strcmp(const char *s1, const char *s2);
extern int strncmp(const char *s1, const char *s2, size_t n);
extern char *strchr(const char *s, int c);
