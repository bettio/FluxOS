#include <arch/ia32/boot/multibootinfo.h>
#include <main.h>

extern "C"
{
    void ia32_start(unsigned long magic, multiboot_info *info)
    {
        if (MultiBootInfo::init(magic, info)){
            main();
        }
    }
}
